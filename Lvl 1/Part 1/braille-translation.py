def solution(s):
    rv = ""
    for c in s:
        ref = [
            '100000', # A
            '110000', # B
            '100100', # C
            '100110', # D
            '100010', # E
            '110100', # F
            '110110', # G
            '110010', # H
            '010100', # I
            '010110', # J
            '101000', # K
            '111000', # L
            '101100', # M
            '101110', # N
            '101010', # O
            '111100', # P
            '111110', # Q
            '111010', # R
            '011100', # S
            '011110', # T
            '101001', # U
            '111001', # V
            '010111', # W
            '101101', # X
            '101111', # Y
            '101011']  # Z
        if c >= 'A' and c <= 'Z':
            cap = '000001'
            val = ref[ord(c) - ord('A')]
        elif c >= 'a' and c <= 'z':
            cap = ''
            val = ref[ord(c) - ord('a')]
        else:
            cap = ''
            val = '000000'
        rv += cap + val
    return rv